 // Pede um número inteiro ao usuário
 const numero = parseInt(prompt("Digite um número inteiro:"));


 // Calcula o quadrado do número
 const quadrado = numero * numero;

 // Exibe o resultado
 console.log(`O quadrado do numero ${numero} é ${quadrado}.`);

 // resultado como alert também
 alert(`${numero} ao quadrado é ${quadrado}.`);