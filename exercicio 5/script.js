 // Pede dois números inteiros ao usuário
 const numero1 = parseInt(prompt("Digite um número inteiro:"));
 const numero2 = parseInt(prompt("Digite um número inteiro:"));


 // Calcula a soma dos números
 const soma = numero1 + numero2;

 // Exibe o resultado
 console.log(`A soma dos numeros ${numero1} e ${numero2} é ${soma}.`);

 // resultado como alert também
 alert(`${numero1} + ${numero2} = ${soma}.`);